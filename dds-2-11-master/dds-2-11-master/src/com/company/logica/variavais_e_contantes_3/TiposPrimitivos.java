package com.company.logica.variavais_e_contantes_3;

public class TiposPrimitivos {

    public static void main(String[] args) {
        String variavelString = ""; // o tipo String n'ao possui um tipo primitivo equivalente

        byte variavelByte = 1; // o tipo byte, assim como os outros primitivos,
                               // n'ao pode manter um estado de ausencia de valor.
                               // Lembrando que aus"encia de valor [e representado pela palavra null
                               // que significa nulo.

        short variavelShort = 1;

        int variavelInteger = 1; // o tipo normal de int [e Integer

        long variavelLong = 1L;

        float variavelFloat = 1.0f;

        double variavelDouble = 1.0;

        char variavelCharacter = 'A'; // o tipo normal de char [e Character

        boolean variavelBoolean = true;
    }
}
